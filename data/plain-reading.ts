export function getPlainHexagramSummary(name: string) {
  return hexagramPlainSummaries[name] ?? "此卦暂未完成精校白话，先以原文和动爻为主判断。";
}

export function getPlainLineSummary(lineText: GaodaoLineText | undefined, topic: string, hexagramName?: string) {
  const yaoCi = extractYaoCi(lineText?.original ?? "");
  const position =
    (hexagramName ? lineOverrides[hexagramName]?.[lineText?.line ?? 0] : undefined) ??
    linePlainSummaries[lineText?.line ?? 0] ??
    "此爻是本次变化点，需要结合原文判断。";
  const topicPrompt = getTopicDescription(topic);

  return {
    yaoCi,
    summary: `${position}分类角度：${topicPrompt}`,
  };
}
